import type { DailyGuidePlace, SwissGuideData } from "./swiss-guide-data";

function place(
  id: string,
  name: string,
  category: string,
  description: string,
  lat: number,
  lng: number,
  image: string,
  timeLabel?: string
): DailyGuidePlace {
  const guideCategory = getSicilyGuideCategory(name, category);
  const detail = getSicilyPlaceDetail(name, guideCategory, description);

  return {
    id,
    name,
    category: guideCategory,
    timeLabel,
    description,
    shortDescription: description,
    detailDescription: detail,
    whyVisit: getSicilyWhyVisit(name, guideCategory),
    whatToSee: getSicilyWhatToSee(name, guideCategory),
    tips: getSicilyTips(name, guideCategory),
    duration: getSicilyDuration(name, guideCategory),
    coordinates: { lat, lng },
    image,
    imageAlt: `${name} travel image`,
    googleMapsUrl: `https://www.google.com/maps/search/?api=1&query=${encodeURIComponent(name)}`,
    mapPosition: { x: 50, y: 50 }
  };
}

function normalizePlaceKey(name: string) {
  return name.toLowerCase().replace(/[^a-z0-9가-힣]+/g, " ").trim();
}

